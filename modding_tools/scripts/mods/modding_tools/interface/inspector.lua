mod.inspector = inspector:new()

-- ##### ┬┌┐┌┌┬┐┌─┐┬─┐┌─┐┌─┐┌─┐┌─┐ ####################################################################################
-- ##### ││││ │ ├┤ ├┬┘├┤ ├─┤│  ├┤  ####################################################################################
-- ##### ┴┘└┘ ┴ └─┘┴└─└  ┴ ┴└─┘└─┘ ####################################################################################

mod.inspect = function(self, key, obj)
    if self.inspector and not self.inspector.destroyed then
        self.inspector:clear()
        self.inspector:navigate(key, obj)
        self.inspector:show(true)
    end
end