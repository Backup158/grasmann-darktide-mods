mod.watcher = watcher:new()

-- ##### ┬┌┐┌┌┬┐┌─┐┬─┐┌─┐┌─┐┌─┐┌─┐ ####################################################################################
-- ##### ││││ │ ├┤ ├┬┘├┤ ├─┤│  ├┤  ####################################################################################
-- ##### ┴┘└┘ ┴ └─┘┴└─└  ┴ ┴└─┘└─┘ ####################################################################################

mod.watch = function(self, ...)
    self.watcher:watch(...)
end

mod.watcher_set_mod = function(self, mod)
    self.watcher:set_mod(mod)
end

mod.watcher_hotkey = function()
    mod.watcher:toggle()
end

mod.watcher_show = function(self, show)
    self.watcher:show(show)
end